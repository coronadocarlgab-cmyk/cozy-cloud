// Data Shape
type Activity = {
  id: string
  day_number: number
  start_time: string
  activity_name: string
  location: string
  notes: string
}

type ItineraryDocumentProps = {
  destination: string
  activities: Activity[]
}

export default function ItineraryDocument({ destination, activities }: ItineraryDocumentProps) {
  // Group by day
  const grouped = activities.reduce((acc, curr) => {
    const day = curr.day_number
    if (!acc[day]) acc[day] = []
    acc[day].push(curr)
    return acc
  }, {} as Record<number, Activity[]>)

  return (
    <Document>
      <Page size="A4" style={styles.page}>
        
        {/* Header */}
        <View style={styles.header}>
          <Text style={styles.title}>Trip to {destination}</Text>
          <Text style={styles.subtitle}>Generated by Cozy Cloud ‚Ä¢ The Academic Hub</Text>
        </View>

        {/* Content */}
        {Object.keys(grouped).map((day) => (
          <View key={day} style={styles.daySection}>
            <Text style={styles.dayHeader}>Day {day}</Text>
            
            {grouped[parseInt(day)].map((act) => (
              <View key={act.id} style={styles.activityRow}>
                <Text style={styles.time}>{act.start_time}</Text>
                <View style={styles.details}>
                  <Text style={styles.activityName}>{act.activity_name}</Text>
                  {act.location && <Text style={styles.location}>{act.location}</Text>}
                </View>
              </View>
            ))}
          </View>
        ))}

        {/* Footer */}
        <Text style={styles.footer}>
          "The world is a book and those who do not travel read only one page." üåç
        </Text>
      </Page>
    </Document>
  )
}